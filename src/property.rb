class Property

  def initialize(name, bedroom_count, latitude, longitude)
    @name = name
    @bedroom_count = bedroom_count
    @latitude = latitude
    @longitude = longitude
  end

  def name
    @name
  end

  def bedroom_count
    @bedroom_count
  end

  def latitude
    @latitude
  end

  def longitude
    @longitude
  end

end
